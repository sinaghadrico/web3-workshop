export const toChecksumAddress = async (address: string) => {
  return ethers.getAddress(address);
};

export const verifyMessage = async (
  message: any,
  address: any,
  signature: any
) => {
  try {
    const signerAddress = await ethers.verifyMessage(message, signature);
    if (toChecksumAddress(signerAddress) !== toChecksumAddress(address)) {
      return false;
    }
    return true;
  } catch (error) {
    return false;
  }
};

